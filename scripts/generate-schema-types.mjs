#!/usr/bin/env node
// Regenerates packages/core/src/generated/domain.ts from every schema file
// under schemas/. This is the concrete implementation of the stack
// requirement to "avoid duplicating authoritative domain definitions:
// generate language models ... from the JSON Schemas". Do not hand-edit the
// output; edit the schemas and re-run `pnpm run generate:types`.
//
// Each schema is fully dereferenced and compiled independently, then its
// output is wrapped in its own TypeScript namespace so that shared
// sub-schema names (ActorRef, Digest, UUID, ArtifactEnvelope, ...) can be
// declared once per namespace without colliding with the same names
// declared in another schema's namespace. The schema's own top-level type
// is then re-exported at module scope under its natural name, which is all
// downstream code needs to import.
import { readdirSync, statSync, readFileSync, writeFileSync, mkdirSync } from 'node:fs';
import path from 'node:path';
import { fileURLToPath } from 'node:url';
import $RefParser from '@apidevtools/json-schema-ref-parser';
import { compile } from 'json-schema-to-typescript';

const ROOT = path.resolve(fileURLToPath(import.meta.url), '../..');
const SCHEMAS_DIR = path.join(ROOT, 'schemas');
const OUT_FILE = path.join(ROOT, 'packages/core/src/generated/domain.ts');
const ID_PREFIX = 'https://schemas.act-protocol.org/1.0/';

function walk(dir, out = []) {
  for (const entry of readdirSync(dir).sort()) {
    const full = path.join(dir, entry);
    if (statSync(full).isDirectory()) {
      if (entry === 'fixtures') continue;
      walk(full, out);
    } else if (entry.endsWith('.schema.json')) {
      out.push(full);
    }
  }
  return out;
}

function namespaceFor(file) {
  return (
    'NS_' +
    path
      .relative(SCHEMAS_DIR, file)
      .replace(/\.schema\.json$/, '')
      .replace(/[\\/.-]/g, '_')
  );
}

function typeNameFor(file) {
  const doc = JSON.parse(readFileSync(file, 'utf8'));
  const base = doc.title || path.basename(file, '.schema.json');
  return base.replace(/[^a-zA-Z0-9]/g, '');
}

async function main() {
  const files = walk(SCHEMAS_DIR);
  const idToFile = new Map();
  for (const f of files) {
    const doc = JSON.parse(readFileSync(f, 'utf8'));
    if (doc.$id) idToFile.set(doc.$id, f);
  }

  const actResolver = {
    order: 1,
    canRead: (fileInfo) => fileInfo.url.startsWith(ID_PREFIX),
    read: (fileInfo) => {
      const file = idToFile.get(fileInfo.url);
      if (!file) throw new Error(`Unresolvable $ref: ${fileInfo.url}`);
      return readFileSync(file, 'utf8');
    },
  };

  const chunks = [];
  const exportLines = [];

  for (const file of files) {
    const rel = path.relative(SCHEMAS_DIR, file);
    const raw = JSON.parse(readFileSync(file, 'utf8'));
    const name = typeNameFor(file);
    const ns = namespaceFor(file);

    const dereferenced = await $RefParser.dereference(structuredClone(raw), {
      resolve: { act: actResolver, http: false, https: false },
    });

    const ts = await compile(dereferenced, name, {
      bannerComment: '',
      additionalProperties: false,
      style: { semi: true, singleQuote: true },
    });

    const indented = ts
      .split('\n')
      .map((line) => (line.length ? '  ' + line : line))
      .join('\n');

    chunks.push(`// source: schemas/${rel}\nnamespace ${ns} {\n${indented}\n}`);
    exportLines.push(`export type ${name} = ${ns}.${name};`);
  }

  const banner = `/**
 * AUTO-GENERATED by scripts/generate-schema-types.mjs from every schema file
 * under schemas/. Do not hand-edit. Re-run: pnpm run generate:types
 */
/* eslint-disable */
`;

  const body = banner + chunks.join('\n\n') + '\n\n' + exportLines.join('\n') + '\n';

  mkdirSync(path.dirname(OUT_FILE), { recursive: true });
  writeFileSync(OUT_FILE, body);
  console.log(`Generated ${files.length} schema-backed types -> ${path.relative(ROOT, OUT_FILE)}`);
}

main().catch((err) => {
  console.error(err);
  process.exit(1);
});
